class Book {
  final String? title;
  final String? author;
  final String? description;
  final String? imageUrl;
  final String? publishedDate;
  final int? pageCount;
  final String? publisher;
  final List<String>? categories;
  final String? buyLink;

  Book({
    this.title,
    this.author,
    this.description,
    this.imageUrl,
    this.publishedDate,
    this.pageCount,
    this.publisher,
    this.categories,
    this.buyLink,
  });
}